int	ft_atoi(const char *nptr)
{
	int	r;
	int	sign;
	int	i;

	r = 0;
	i = 0;
	sign = 1;
	if (nptr[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		r = (r * 10) + (nptr[i] - '0');
		i++;
	}
	r = r * sign;
	return (r);
}
